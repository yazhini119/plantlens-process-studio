const OIP_MODEL_ROOT = '/open-industry/models'

function createOipStencil(definition) {
  const parameters = [
    'Asset Source',
    'Tag Mapping',
    'Comms Enabled',
    'PLC/OPC UA Tag',
    'Health Status',
    ...(definition.parameters ?? []),
  ]

  return {
    family: 'machine',
    renderer: 'openIndustryAsset',
    source: 'Open Industry Project',
    rules: {},
    anchors: [{ id: 'callout', kind: 'callout', position: [0, 2.15, 0] }],
    ports: [
      { id: 'inlet', label: 'Input', direction: 'in', medium: definition.medium ?? 'signal', required: false, position: [-0.5, 0.1, 0] },
      { id: 'outlet', label: 'Output', direction: 'out', medium: definition.medium ?? 'signal', required: false, position: [0.5, 0.1, 0] },
    ],
    ...definition,
    defaults: {
      parameters: {
        'Asset Source': 'Open Industry Project',
        'Tag Mapping': 'Unmapped',
        'Comms Enabled': 'No',
        'PLC/OPC UA Tag': 'Not assigned',
        'Health Status': 'Ready',
        ...(definition.defaults?.parameters ?? {}),
      },
      metrics: definition.defaults?.metrics ?? {},
    },
    parameters,
    asset: {
      modelUrl: `${OIP_MODEL_ROOT}/${definition.asset.file}`,
      scale: definition.asset.scale ?? [1, 1, 1],
      rotation: definition.asset.rotation ?? [0, 0, 0],
      position: definition.asset.position ?? [0, 0, 0],
    },
  }
}

